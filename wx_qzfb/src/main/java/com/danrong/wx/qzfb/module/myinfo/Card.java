package com.danrong.wx.qzfb.module.myinfo;


public class Card {

  private String card_id;// 唯一标识
  private String wex_id;// 用户微信号
  private String patient_name;// 就诊人姓名
  private String card_no;// 患者卡号
  private String telephone;// 患者手机号
  private boolean dft;// 默认
  private String image;// 头像
  private boolean hasImage;// 是否存在头像
  private String c_type;// 卡类型
  private String sex;// 性别
  private String birthday;// 生日
  private String address;// 地址

  public String getCard_id() {
    return card_id;
  }

  public void setCard_id(String card_id) {
    this.card_id = card_id;
  }

  public String getWex_id() {
    return wex_id;
  }

  public void setWex_id(String wex_id) {
    this.wex_id = wex_id;
  }

  public String getPatient_name() {
    return patient_name;
  }

  public void setPatient_name(String patient_name) {
    this.patient_name = patient_name;
  }

  public String getCard_no() {
    return card_no;
  }

  public void setCard_no(String card_no) {
    this.card_no = card_no;
  }

  public String getTelephone() {
    return telephone;
  }

  public void setTelephone(String telephone) {
    this.telephone = telephone;
  }

  public boolean isDft() {
    return dft;
  }

  public void setDft(boolean dft) {
    this.dft = dft;
  }

  public String getImage() {
    return image;
  }

  public void setImage(String image) {
    this.image = image;
  }

  public boolean isHasImage() {
    return hasImage;
  }

  public void setHasImage(boolean hasImage) {
    this.hasImage = hasImage;
  }

  public String getC_type() {
    return c_type;
  }

  public void setC_type(String c_type) {
    this.c_type = c_type;
  }

  public String getSex() {
    return sex;
  }

  public void setSex(String sex) {
    this.sex = sex;
  }

  public String getBirthday() {
    return birthday;
  }

  public void setBirthday(String birthday) {
    this.birthday = birthday;
  }

  public String getAddress() {
    return address;
  }

  public void setAddress(String address) {
    this.address = address;
  }

}
