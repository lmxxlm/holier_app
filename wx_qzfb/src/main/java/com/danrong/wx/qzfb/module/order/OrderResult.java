package com.danrong.wx.qzfb.module.order;

import java.io.Serializable;

public class OrderResult implements Serializable {

  private static final long serialVersionUID = -2661956708166712682L;

  private String resultCode;// 返回码
  private String resultContent;// 返回内容
  private String orderCode;// 预约单号
  private String seqCode;// 就诊序号
  private String regFee;// 挂号费
  private String orderContent;// 预约内容

  public String getResultCode() {
    return resultCode;
  }

  public void setResultCode(String resultCode) {
    this.resultCode = resultCode;
  }

  public String getResultContent() {
    return resultContent;
  }

  public void setResultContent(String resultContent) {
    this.resultContent = resultContent;
  }

  public String getOrderCode() {
    return orderCode;
  }

  public void setOrderCode(String orderCode) {
    this.orderCode = orderCode;
  }

  public String getSeqCode() {
    return seqCode;
  }

  public void setSeqCode(String seqCode) {
    this.seqCode = seqCode;
  }

  public String getRegFee() {
    return regFee;
  }

  public void setRegFee(String regFee) {
    this.regFee = regFee;
  }

  public String getOrderContent() {
    return orderContent;
  }

  public void setOrderContent(String orderContent) {
    this.orderContent = orderContent;
  }

}
